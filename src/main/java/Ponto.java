public class Ponto{
    double x;
    double y;

    public double getX() {
        return x;
    }
    public void setX(double x) {
        this.x = x;
    }
    public double getY() {
        return y;
    }
    public void setY(double y) {
        this.y = y;
    }
    public Ponto(double x, double y) {
        this.x = x;
        this.y = y;
    }
    public void XY(double x, double y){
        this.x = x;
        this.y = y;

    }
}